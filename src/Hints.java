
public class Hints {

public int key = 0;
public String value = null;
public String time = null;
	
public void setKey(int key){
this.key = key;
}
public void setValue(String value){
this.value = value;
}
public void setTime(String time){
this.time = time;
}

public int getKey(){
return key;
}
public String getValue(){
return value;
}
public String getTime(){
return time;
}

}
